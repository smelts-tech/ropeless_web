  # override `resource_class` to look at params[:user][:access_needed] and return the right class
  def resource_class
    devise_mapping.to unless action_name == "create"

    access_needed = params.dig(:user, :access_needed)
    case access_needed
      when "AgencyUser"
        AgencyUser
      when "Fisher"
        Fisher
